//ASSIGNMENT OPERATORS

let r = 5;
//our equal sign is the assignment

// addition
r += 1; //r + 1 OR 5+1 because r=5
console.log(r);

//SUBTRACTION

r -= 2; // r=r - 2
console.log(r);

//MULTIPLICATION

r *= 2;
console.log(r);

//DIVISION
r /= 1;
console.log(r);

//EXPONENTIAL
r **= 2; //r*r
console.log(r);

//REMAINDER
r %= 1;
console.log(r);